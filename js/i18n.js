class I18n {
  constructor() {
    this.lang = localStorage.getItem('artemis-lang') || this._detectLang();
    if (!TRANSLATIONS[this.lang]) this.lang = 'en';
    document.documentElement.lang = this.lang;
  }

  _detectLang() {
    const browserLang = (navigator.language || 'en').slice(0, 2);
    return TRANSLATIONS[browserLang] ? browserLang : 'en';
  }

  t(key, fallback = key) {
    return TRANSLATIONS[this.lang]?.[key] ?? TRANSLATIONS.en?.[key] ?? fallback;
  }

  setLang(lang) {
    if (!TRANSLATIONS[lang]) return;
    this.lang = lang;
    localStorage.setItem('artemis-lang', lang);
    document.documentElement.lang = lang;
    this.applyToDOM();
    window.dispatchEvent(new Event('i18n-changed'));
  }

  applyToDOM() {
    // Replace text for all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      el.innerHTML = this.t(key);
    });
    // Placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.placeholder = this.t(el.dataset.i18nPlaceholder);
    });
    // Titles / tooltips
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      el.title = this.t(el.dataset.i18nTitle);
    });
  }
}

window.i18n = new I18n();
